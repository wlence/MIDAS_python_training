numbers = [1, 2, 3, 4, 5, 6, 7, 8]

print(len(numbers))  # Output: 8
print(numbers[0])  # Output: 1
print(numbers[-1])  # Output: 8

# Slicing a list
print(numbers[1:4])  # Output: [2, 3, 4]
print(numbers[:4])  # Output: [1, 2, 3, 4]
print(numbers[4:])  # Output: [5, 6, 7, 8]
print(numbers[::2])  # Output: [1, 3, 5, 7]
print(numbers[1::2])  # Output: [2, 4, 6, 8]
print(numbers[::-1])  # Output: [8, 7, 6, 5, 4, 3, 2, 1]

names = ["Alice", "Bob", "Charlie", "Dave", "Eve"]
print(names[1:4])  # Output: ["Bob", "Charlie", "Dave"]
print(names[:3])  # Output: ["Alice", "Bob", "Charlie"]

